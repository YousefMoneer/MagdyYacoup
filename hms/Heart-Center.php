                    <br>
                    <p>كما يكرس مركز أسوان للقلب مختلف الجهود والموارد المتاح لـ قسم البحث العلمي، ليفتح بابًا لتطبيق مجموعة واسعة من فرص البحث، بما في ذلك
                        الهندسة الحيوية وعلوم الحياة، لتحسين وتطوير الرعاية الطبية لدينا وضمان توفير تجربة علاج متطورة وشاملة للمرضى.</p>
                    <br>
                    <p>وتماشيًا مع هذه الرؤية، بدأت مؤسسة مجدي يعقوب لأمراض وأبحاث القلب مؤخرًا في تنفيذ مشروع كبير واستثنائي 
                        له أهمية كبيرة في صحة ورفاهية الأفراد في مصر والمنطقة بأكملها، وهو مركز مجدي يعقوب العالمي للقلب بالقاهرة. وسيعمل 
                        مركز مجدي يعقوب العالمي للقلب الجديد بالقاهرة على تطوير رعاية القلب وعلاجه، بالإضافة إلى توسيع نطاق التدريب لدينا وقدرتنا على البحث والتطوير.</p>
                    <br>
                    <p>كما إن الهدف الأساسي من بناء مركز مجدي يعقوب العالمي للقلب بالقاهرة، هو تقديم نموذج ومركز متميز على أفضل المستويات العالمية في علاج القلب بشكل مجاني
                        بالكامل؛ من أجل تحسين مستوى الرعاية الطبية المقدمة للأطفال والشباب والكبار كذلك في المنطقة بأكملها، على أمل إنقاذ وشفاء أكبر عدد ممكن من القلوب المريضة
                        لعلاج أكبر عدد من الحالات المعقدة، إلى جانب تقديم التدريب وإجراء البحوث الرائدة التي ستفيد الأجيال القادمة.</p>
